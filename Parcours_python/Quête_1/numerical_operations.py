def add(a,b):
    return a+b

def subtract(a,b):
    return a-b

def multiply(a,b):
    return a*b

def power(a,b):
    return a**b

def square(a):
    return a**2

def modulo(a,b):
    if b == 0:
        return 0
    return a%b

def divide(a,b):
    if b == 0:
        return 0
    return a/b

def integer_division(a,b):
    if b == 0:
        return 0
    return a//b